    Animation(String animName, String animHexCode) {
        this.animName = animName;
        this.animHexCode = animHexCode;
        int top = Character.digit(animHexCode.charAt(0), 16);
        int bot = Character.digit(animHexCode.charAt(1), 16);
        this.animCode= (top == -1 || bot == -1)?255: (top << 4) + bot;

    }

    private final String animName;
    private final String animHexCode;
    private final int animCode;

    public static Animation getByHexCode(String hexCode) throws Exception {
        for (Animation aType: Animation.values()) {
            if (aType.animHexCode.equalsIgnoreCase(hexCode)) {
                return aType;
            }
        }
       throw new Exception("Unknown Animation hexadecimal code " + hexCode);
    }

    public static Animation getByCode(int code) throws Exception {
        for (Animation aType: Animation.values()) {
            if (aType.animCode == code) {
                return aType;
            }
        }
        throw new Exception("Unknown Animation code " + code);
    }

    public static Animation getByName(String name) throws Exception {
        for (Animation aType: Animation.values()) {
            if (aType.animName.equalsIgnoreCase(name)) {
                return aType;
            }
        }
        throw new Exception("Unknown Animation named '" + name +"'");
    }

    public String getName() { return animName;}
    public String getHexCode() { return animHexCode;}
    public int getCode() { return animCode;}

